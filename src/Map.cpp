Map::Map() {
    sheet = TextureManager::LoadTexture("assets/tileMap.png");

    LoadMap(lvl1);

    srcStone.h = srcGrass.h = 32;
    srcStone.w = srcGrass.w = 32;
    srcStone.x = 0; srcGrass.x = 32;
    srcStone.y = srcGrass.y = 0;

    dest.h = 32;
    dest.w = 32;
}

void Map::LoadMap(int arr[20][25]) {
    for(int row = 0; row < 20; row++) {
        for(int col = 0; col < 25; col++) {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::DrawMap() {
    int type = 0;
    for(int row = 0; row < 20; row++) {
        for(int col = 0; col < 25; col++) {
            type = map[row][col];

            dest.x = col * 32;
            dest.y = row * 32;

            switch(type) {
                case 0:
                    TextureManager::Draw(sheet, srcStone, dest);
                    break;
                case 1:
                    TextureManager::Draw(sheet, srcGrass, dest);
                    break;

                default:
                    break;
            }
        }
    }
}